/* ==========================================
   DOWNLOAD PDF
========================================== */

function downloadPDF() {

    if (typeof html2pdf === "undefined") {

        alert(

            "html2pdf.js is not loaded."

        );

        return;

    }

    showLoading();

    const container = document.createElement("div");

    container.innerHTML = buildReportHTML();

    const candidate =
        document.getElementById("candidateName")?.value || "Candidate";

    const week =
        document.getElementById("week")?.value || "Week";

    const options = {

        margin: 10,

        filename:
            `${candidate}_FPI_${week}.pdf`,

        image: {

            type: "jpeg",

            quality: 1

        },

        html2canvas: {

            scale: 2,

            useCORS: true

        },

        jsPDF: {

            unit: "mm",

            format: "a4",

            orientation: "portrait"

        }

    };

    html2pdf()

        .set(options)

        .from(container)

        .save()

        .then(() => {

            hideLoading();

        })

        .catch(error => {

            hideLoading();

            console.error(error);

            alert("Unable to generate PDF.");

        });

}

/* ==========================================
   BUTTON BINDING
========================================== */

function initializePDF() {

    const button =
        document.getElementById("downloadBtn");

    if (!button) return;

    button.addEventListener(

        "click",

        downloadPDF

    );

}