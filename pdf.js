function getReportData() {

    const result = calculateFPI();

    return {

        candidate:
            document.getElementById("candidateName")?.value || "",

        batch:
            document.getElementById("batch")?.value || "",

        week:
            document.getElementById("week")?.value || "",

        mentor:
            document.getElementById("mentor")?.value || "",

        learning: result.learning,

        reinforcement: result.reinforcement,

        performance: result.performance,

        learningPercent:
            result.learningPercent.toFixed(1),

        reinforcementPercent:
            result.reinforcementPercent.toFixed(1),

        performancePercent:
            result.performancePercent.toFixed(1),

        fpi:
            result.fpi.toFixed(1),

        status:
            result.status.text

    };

}

